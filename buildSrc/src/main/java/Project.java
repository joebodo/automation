package automation;

public class Project {

	String hacLogin;
	String hacPassword;
	String hostHacUrl;
	public String getHacLogin() {
		return hacLogin;
	}
	public void setHacLogin(String hacLogin) {
		this.hacLogin = hacLogin;
	}
	public String getHacPassword() {
		return hacPassword;
	}
	public void setHacPassword(String hacPassword) {
		this.hacPassword = hacPassword;
	}
	public String getHostHacUrl() {
		return hostHacUrl;
	}
	public void setHostHacUrl(String hostHacUrl) {
		this.hostHacUrl = hostHacUrl;
	}

}
